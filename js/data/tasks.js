// Функция получения случайных заданий
function getRandomTasks(type, count = 5) {
    const typeTasks = TasksDB[type];
    if (!typeTasks || typeTasks.length === 0) return [];
    
    const shuffled = Utils.shuffleArray([...typeTasks]);
    return shuffled.slice(0, Math.min(count, shuffled.length));
}

